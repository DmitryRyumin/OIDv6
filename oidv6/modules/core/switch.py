# ######################################################################################################################
# Переключатель
# ######################################################################################################################
class Switch(object):
    """Переключатель"""

    # ------------------------------------------------------------------------------------------------------------------
    # Конструктор
    # ------------------------------------------------------------------------------------------------------------------

    def __init__(self, value):
        self.value = value
        self.fall = False

    # ------------------------------------------------------------------------------------------------------------------
    # Итератор
    # ------------------------------------------------------------------------------------------------------------------

    def __iter__(self):
        yield self.match
        raise StopIteration

    # ------------------------------------------------------------------------------------------------------------------
    # Внешние методы
    # ------------------------------------------------------------------------------------------------------------------

    def match(self, *args):
        if self.fall or not args:
            return True
        elif self.value in args:
            self.fall = True
            return True
        else:
            return False
